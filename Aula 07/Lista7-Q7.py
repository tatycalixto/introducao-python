def operacoes_basicas(num1, num2):
    soma = num1 + num2
    subtracao = num1 - num2
    divisao = num1 / num2
    multiplicacao = num1 * num2

    print("Soma:", soma)
    print("Subtração:", subtracao)
    print("Divisão:", divisao)
    print("Multiplicação:", multiplicacao)

operacoes_basicas(10, 5)
